function fetchItemsToDisplay(items, sortParameter, sortOrder, itemsPerPage, pageNumber) {
  // Write your code here
  const x = items.sort((a, b) => {
    const aX = sortParameter ? +a[sortParameter] : a[sortParameter]
    const bX = sortParameter ? +b[sortParameter] : b[sortParameter]
    let returnVal = 0
    if (aX > bX) returnVal = 1
    if (aX < bX) returnVal = -1
    return !sortOrder ? returnVal : (-1 * returnVal)
  })
  const sortedItems = x.map(([name]) => name)
  console.log(sortedItems.slice(0, itemsPerPage))
  let count = 0
  while (sortedItems.length) {
    const page = []
    for (let i = 0; i < itemsPerPage; i++) {
      page.push(sortedItems.shift())
    }
    if (count === pageNumber) {
      return page
    }
    count++
  }
  return []
}

